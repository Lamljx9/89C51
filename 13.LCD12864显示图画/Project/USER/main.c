/*************************************************************************
 Function    : 主程序
 Description : None 
**************************************************************************/
void main()
{
	u8 i,line,columm;
	u8 *address;
	
	Lcd_Init();                                       // 初始化
	
	while(1)
	{
		/* 第1行显示内容 */
		line = 0;                                     // 第一行
		columm = 0;                                   // 从0列开始显示
		address = table1;                             // 第一行显示 万物互联 16*16
		On_Off(0);                                    // 关显示
		
		for(i=1;i<3;i++)                              // 图画2
		{
			if(i<2)                                   // 左半屏显示1幅
			{
				Select_Screen(1);                     // 1 左屏幕
			}
			else Select_Screen(2);                    // 1 右屏幕
			
			Show_6464(line,columm,address);           // 显示一个64*64图画

			columm += 64;                             // 一幅图的宽度
			
			if(columm>63)                             // 控制左右半屏中间空格 -> 大于63即显示右半屏  中间无空格
			{ 
				columm = 0;
			}
			address += 512;                            // 地址加1   (总是等于点阵的个数)
		}
		

		

		Select_Screen(0);                              // 全屏显示
		On_Off(1);                                     // 开显示
		
		delay_ms(100000);                              // 延时
	}
}