/*************************************************************************
 Function    : 主程序
 Description : None 
**************************************************************************/
void main()
{
	u8 i,line,columm;
	u8 *address;
	
	Lcd_Init();                                       // 初始化
	
	while(1)
	{
		/* 第1行显示内容 */
		line = 0;                                     // 第一行
		columm = 32;                                  // 从32列开始显示
		address = table1;                             // 第一行显示 万物互联 16*16
		On_Off(0);                                    // 关显示
		
		for(i=1;i<5;i++)                              // 总汉字个数4
		{
			if(i<3)                                   // 左半屏显示2个
			{
				Select_Screen(1);                     // 前2个汉字 左屏幕
			}
			else Select_Screen(2);                    // 后2个汉字 右屏幕
			
			Show_1616(line,columm,address);           // 显示一个16*16汉字

			columm += 16;                             // 一个汉字的宽度
			
			if(columm>63)                             // 控制左右半屏中间空格 -> 第32列开始 + 万16 + 物16 = 64  大于63即显示右半屏  中间无空格
			{ 
				columm = 0;
			}
			address += 32;                            // 地址加1   (总是等于点阵的个数)
		}
		
		/* 第2行显示内容 */
		line = 2;                                     // 第3行
		columm = 8;                                   // 从8列开始显示
		address = table2;                             // 第二行显示 万物互联 24*24
		On_Off(0);                                    // 关显示
		
		for(i=1;i<5;i++)                              // 总汉字个数4
		{
			if(i<3)                                   // 左半屏显示2个
			{ 
				Select_Screen(1);                      // 前2个汉字 左屏幕
			}
			else Select_Screen(2);                    // 后2个汉字 右屏幕
			 
			Show_2424(line,columm,address);            // 显示一个24*24汉字
			
			columm += 24;                              // 一个汉字的宽度
			
			if(columm>55)                              // 控制左右半屏中间空格 -> 第8列开始 + 万24 + 物24 = 56  大于55即显示右半屏  中间空格8
			{
				columm = 0;
			}
			address += 72;                             // 地址加1  (总是等于点阵的个数)
		}		
		
		
		
		/* 第3行显示内容 */
		line = 5;                                      // 第5行
		columm = 32;                                   // 从32列开始显示
		address = table3;                              // 第三行显示 LOADING... 16*08
		On_Off(0);                                     // 关显示
		
		for(i=1;i<11;i++)                              // 总字母个数10
		{
			if(i<5)                                    // 左半屏显示4个
			{
				Select_Screen(1);                       // 前4个字母 左屏幕
			}
			else Select_Screen(2);                     // 后6个字母 右屏幕
			
			Show_1608(line,columm,address);            // 显示一个16*08字母
			
			columm += 8;                               // 一个汉字的宽度
			
			if(columm>63)                              // 控制左右半屏中间空格 -> 第32列开始 + (字母8*4) = 64  大于63即显示右半屏  中间空格0
			{
				columm = 0;
			}
			address += 16;                             // 地址加1  (总是等于点阵的个数)
		}		
		

		Select_Screen(0);                              // 全屏显示
		On_Off(1);                                     // 开显示
		
		delay_ms(100000);                              // 延时
	}
}